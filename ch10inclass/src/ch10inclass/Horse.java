package ch10inclass;

public class Horse {
	
		String name;
		String color;
		int birth;
		

		public String getname() {
			return name;
		}
		public void setname(String name) {
			this.name = name;
		}
		public String getcolor() {
			return color;
		}
		public void setcolor(String color) {
			this.color = color;
		}
		public double getbirth() {
			return birth;
		}
		public void setbirth(int birth) {
			this.birth = birth;
		}
		
		

	}